function persianToEnglish(word) {
   if (word === 'تاریخ') {
    return 'history'
   } else if (word === 'فلسفه') {
    return 'philosiphy'
   } else if (word === 'شعر'){
    return 'poetry  '
   } else if(word === 'عاشقانه'){
    return 'romantic'
   } else if (word === 'درام') {
    return 'dram'
   }
}

