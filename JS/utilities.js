function persianToEnglish(word) {
   if (word === 'تاریخ') {
      return 'history'
   } else if (word === 'فلسفه') {
      return 'philosiphy'
   } else if (word === 'شعر') {
      return 'poetry  '
   } else if (word === 'عاشقانه') {
      return 'romantic'
   } else if (word === 'درام') {
      return 'dram'
   } else if (word === 'فارسی') {
      return 'persian'
   } else if (word === 'یونانی') {
      return 'greek'
   } else if (word === 'اسپانیایی') {
      return 'spanish'
   } else if (word === 'انگلیسی') {
      return 'english'
   }
}

